# Print hello world
print('hello world ssdsdd')

# Using variable
message = 'hello world'
print(message)

# print Multiple messages
message1 = 'Message 1'
print(message, message1)

# Handle ' using escape character
message = 'Neelansh\'s world1'
print(message)

# Handle ' Using double quotes
message = "Neelansh's world2"
print(message)

# Multi-line strings
message1 = """Neelansh's world3
Yahooo world"""
print(message1)

message2 = """Neelansh's world4
    Yahooo world"""
print(message2)
